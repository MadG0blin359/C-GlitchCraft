#include<stdio.h>

int main() {
	int a, b; // Variables to store user input

	// Prompt the user to enter first number
	printf("Enter 1st number \n");
	scanf("%d", &a);

	// Prompt the user to enter second number
	printf("Enter 2nd number \n");
	scanf("%d", &b);

	// Display the sum
	printf("The Sum is %d", a + b);

	return 0; 
	
}
